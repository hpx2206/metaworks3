package org.metaworks.example.ide;

public class Activity {
	String activityName;
		
		public String getActivityName() {
			return activityName;
		}
	
		public void setActivityName(String activityName) {
			this.activityName = activityName;
		}
		

	Role role;
	
		public Role getRole() {
			return role;
		}
	
		public void setRole(Role role) {
			this.role = role;
		}
}
